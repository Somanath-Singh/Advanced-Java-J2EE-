package com.clgw.javabeans;

public class MailJavaBeans {
	
	private String fromUser;
	private String FromUserPass;
	private String toUser;
	private String msg;
	
	
	
	
	public MailJavaBeans() {

	}
	
	
	public String getFromUser() {
		return fromUser;
	}
	public void setFromUser(String fromUser) {
		this.fromUser = fromUser;
	}
	public String getFromUserPass() {
		return FromUserPass;
	}
	public void setFromUserPass(String fromUserPass) {
		FromUserPass = fromUserPass;
	}
	public String getToUser() {
		return toUser;
	}
	public void setToUser(String toUser) {
		this.toUser = toUser;
	}


	public String getMsg() {
		return msg;
	}


	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
	

}
